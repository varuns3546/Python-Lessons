# Variables and Primitives

# Variables are used to store data
# This is an example of a variable: 

net_worth = -2

# net_worth is the name of the variable
# the '=', also known as the assignment operator, is used to assign net_worth a value of negative 2

# Primitives are the basic types of data built into python 
# The four types of primitives are integers, floats, booleans and strings 

# Integers, or ints, are any whole number

int_example = 25

# Floats are numbers that hold a decimal value

float_example = 3.14

# Booleans store values of True or False

salad_is_good = False
i_like_soup = True

#Strings store text

name = "Jimothy"
